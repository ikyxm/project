'''
数据类型：
int
float
string
boolean
'''

age = 19    #声明一个名称为age的变量，赋值为19
# <class 'int'>
print(type(age))    #print()属于一个内置函数，负责输出结果

money = 100.5
# <class 'float'>
print(type(money))

name = "yikm"
# <class 'str'>
print(type(name))

#布尔类型：True False
#用于开发中的判断,比如，是否登录成功
login = True
print(type(login))